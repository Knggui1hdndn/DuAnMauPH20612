package khangndph20612.fpoly.pnlib;

public class obj {
    private String name;
    private int img;

    public obj() {
    }

    public obj(String name, int img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
